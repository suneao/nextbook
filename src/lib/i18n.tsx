// ── Context ──────────────────────────────────────────────────────────────

type LocaleContextType = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string) => string;
};

const LocaleContext = createContext<LocaleContextType>({
  locale: "zh-CN",
  setLocale: () => {},
  t: (key: string) => key,
});

// ── Storage ──────────────────────────────────────────────────────────────

const LOCALE_STORAGE_KEY = "nextbook-locale";

function loadLocale(): Locale {
  if (typeof window === "undefined") return "zh-CN";
  try {
    const raw = localStorage.getItem("nextbook-settings");
    if (raw) {
      const settings = JSON.parse(raw);
      if (
        settings.language &&
        ["zh-CN", "en-US", "ja-JP"].includes(settings.language)
      ) {
        return settings.language as Locale;
      }
    }
  } catch {}
  return "zh-CN";
}

// ── Provider ─────────────────────────────────────────────────────────────

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("zh-CN");
  const [hydrated, setHydrated] = useState(false);

  // Sync locale from localStorage after hydration to avoid mismatch
  useEffect(() => {
    // eslint-disable-next-line react-hooks/set-state-in-effect
    setLocaleState(loadLocale());
    setHydrated(true);
  }, []);

  const setLocale = useCallback((next: Locale) => {
    setLocaleState(next);
    if (typeof window !== "undefined") {
      try {
        const raw = localStorage.getItem("nextbook-settings") || "{}";
        const settings = JSON.parse(raw);
        settings.language = next;
        localStorage.setItem("nextbook-settings", JSON.stringify(settings));
      } catch {}
    }
  }, []);

  const t = useCallback(
    (key: string): string => {
      return dictionaries[locale]?.[key] ?? key;
    },
    [locale],
  );

  return (
    <LocaleContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </LocaleContext.Provider>
  );
}

// ── Hook ─────────────────────────────────────────────────────────────────

export function useLocale() {
  return useContext(LocaleContext);
}
